public class VarEx1 {
//변수 선언
    public static void main(String[] args) {
        //변수 선언 이유
        //-값(data)을 저장할 공간을 마련하기 위해서
        //변수 선언 방법
        //변수타입 변수이름 ;(세미콜론)
        int x = 4, y = 2;
        System.out.println(x + y);
    }
}
//변수에는 문자(char, string), 정수(byte, short, int, long), 실수(float, double) 논리(boolean)