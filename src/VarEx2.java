public class VarEx2 {
    public static void main(String[] args) {

        //변수 하나의 값을 저장하기 위한 공간
        //상수 (constant) - 한 번만 값을 저장 가능한 변수
        //리터럴(literal) - 그 자체로 값을 의미하는 것
        //지역변수는 읽기 전에 초기화가 필수.
        final int score;
        score = 200;
        System.out.println(score);
    }
}
